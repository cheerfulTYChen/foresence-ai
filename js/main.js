    function setLang(lang) {
        // 一般文字切換
        $('[data-i18n]').each(function() {
            var key = $(this).data('i18n');
            if (i18n[lang][key]) {
                $(this).text(i18n[lang][key]);
            }
        });
        $('#footer').text(i18n[lang]['footer']);
    }

    // 預設語言
    setLang('zh-TW');

    // 切換語言
    $('.lang-switch').click(function() {
        var lang = $(this).data('lang');
        setLang(lang);
    });

    // 載入獨立頁尾
    $('#footer').load('footer.html');
